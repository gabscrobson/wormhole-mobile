export default class CalcService {
    static addition = (num1 = 0, num2 = 0) => {
        return num1 + num2
    }

    static subtraction = (num1 = 0, num2 = 0) => {
        return num1 - num2
    }

    static multiplication = (num1 = 0, num2 = 0) => {
        return num1 * num2
    }

    static division = (num1 = 0, num2 = 0) => {
        return num1 / num2
    }
}